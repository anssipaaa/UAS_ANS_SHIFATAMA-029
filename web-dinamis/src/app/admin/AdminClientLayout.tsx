function AdminShell({ children }: { children: React.ReactNode }) {
  const { data: session, status } = useSession();
  const pathname = usePathname();

  const navItems = [
    { name: "Overview", href: "/admin", icon: LayoutIcon },
    { name: "Produk", href: "/admin/produk", icon: PackageIcon },
    { name: "Kategori", href: "/admin/kategori", icon: TagIcon },
    { name: "Pesanan", href: "/admin/pesanan", icon: ShoppingBagIcon },
    { name: "Pelanggan", href: "/admin/pelanggan", icon: UsersIcon },
    { name: "Kupon", href: "/admin/kupon", icon: TicketIcon },
    { name: "Artikel", href: "/admin/artikel", icon: NewspaperIcon },
    { name: "Pengaturan", href: "/admin/pengaturan", icon: SettingsIcon },
  ];

  return (
    <div className="admin-root-cute">
      {/* Floating Top Nav (Pill) */}
      <header className="admin-floating-nav">
        <div className="admin-nav-brand">
          <StoreIcon />
          <span>Cipaa 🎀</span>
        </div>
        
        <div className="admin-nav-links-scroll">
          {navItems.map((item) => {
            const Icon = item.icon;
            const isActive = pathname === item.href || (pathname.startsWith(item.href) && item.href !== "/admin");
            return (
              <Link key={item.name} href={item.href} className={`admin-pill-link ${isActive ? "active" : ""}`}>
                <Icon />
                <span>{item.name}</span>
              </Link>
            );
          })}
        </div>

        <button className="admin-nav-logout" onClick={() => signOut({ callbackUrl: "/login" })} title="Keluar">
          <LogoutIcon />
        </button>
      </header>

      {/* Main Content Area */}
      <main className="admin-main-cute">
        {children}
      </main>
    </div>
  );
}
